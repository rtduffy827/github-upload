
# Types of numbers (for python version 3.x): int, float, complex

a = 496 # This is a perfect number
print(type(a))
print(a)

e = 2.718281828
print(type(e))
print(e)

z = 2 - 6.1j 
print(type(z))
print(z) # both real and imaginary parts of z are stored as floats in python even though the real part was input as an int
print(z.real)
print(z.imag)